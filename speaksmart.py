def main() -> None:
    if not TELEGRAM_TOKEN:
        logger.critical("–ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –æ–∫—Ä—É–∂–µ–Ω–∏—è TELEGRAM_TOKEN –Ω–µ –Ω–∞–π–¥–µ–Ω–∞! –ë–æ—Ç –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –∑–∞–ø—É—â–µ–Ω.")
        return

    logger.info("–ó–∞–ø—É—Å–∫ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –±–æ—Ç–∞...")
    start_health_check_server_in_thread()

    persistence = PicklePersistence(filepath="bot_persistence")

    application = Application.builder().token(TELEGRAM_TOKEN).persistence(persistence).build()

    conv_handler = ConversationHandler(
        entry_points=[MessageHandler(filters.Text(["–Ω–æ–≤—ã–π —Ç–µ–∫—Å—Ç"]), start_new_dialogue)],
        states={
            GET_TEXT_FOR_CORRECTION: [
                MessageHandler(filters.Text(["–Ω–æ–≤—ã–π —Ç–µ–∫—Å—Ç"]), start_new_dialogue),
                MessageHandler(filters.TEXT & ~filters.COMMAND, received_text_for_correction)
            ],
            CHOOSE_STYLE: [
                MessageHandler(filters.Text(["–Ω–æ–≤—ã–π —Ç–µ–∫—Å—Ç"]), start_new_dialogue),
                CallbackQueryHandler(style_chosen, pattern='^style_(business|academic|personal|simplified|auto)$')
            ],
            DESCRIBE_ADDRESSEE: [
                MessageHandler(filters.Text(["–Ω–æ–≤—ã–π —Ç–µ–∫—Å—Ç"]), start_new_dialogue),
                MessageHandler(filters.TEXT & ~filters.COMMAND, addressee_described)
            ],
            POST_PROCESSING_MENU: [
                MessageHandler(filters.Text(["–Ω–æ–≤—ã–π —Ç–µ–∫—Å—Ç"]), start_new_dialogue),
                CallbackQueryHandler(post_processing_action, pattern='^(adjust_(softer|harder|more_formal)|regenerate_text)$')
            ],
        },
        fallbacks=[CommandHandler('cancel', cancel_conversation)],
    )

    application.add_handler(conv_handler)
    application.add_handler(CommandHandler("start", start))
    application.add_handler(CommandHandler("status", status))

    logger.info("–ë–æ—Ç Telegram —É—Å–ø–µ—à–Ω–æ –Ω–∞—Å—Ç—Ä–æ–µ–Ω –∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –≤ —Ä–µ–∂–∏–º–µ –æ–ø—Ä–æ—Å–∞...")
    application.run_polling()


if __name__ == "__main__":
    main()