            "–ü–æ–º–æ–≥—É —Å–¥–µ–ª–∞—Ç—å —Ç–≤–æ–π —Ç–µ–∫—Å—Ç –ª—É—á—à–µ. –ù–∞–∂–º–∏ ¬´–Ω–æ–≤—ã–π —Ç–µ–∫—Å—Ç¬ª, —á—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å üëá"
